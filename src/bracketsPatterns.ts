const newPatterns = []
for (let i = 0; i < patterns.length; i++) {
  const v = patterns[i]
    .replace(/\+/g, '${O}')
    .replace(/1/g, '${a}')
    .replace(/2/g, '${b}')
    .replace(/3/g, '${c}')
    .replace(/4/g, '${d}')
    .replace(/5/g, '${e}')
    .replace(/\${O}/, '${O1}')
    .replace(/\${O}/, '${O2}')
    .replace(/\${O}/, '${O3}')
    .replace(/\${O}/, '${O4}')
  newPatterns.push(v)
}
